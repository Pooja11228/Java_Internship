    //check if the registration id exist or not
    public void checkRegstration() {
     
       
    try{
     st = con.createStatement();
     rs = st.executeQuery("select * from STUDENTQUIZ where REGS_ID ='"+regsId+"' ");
        if(rs.next()){
              selectedCourse=rs.getString("COURSE");
              isCompleted = rs.getString("TESTCOMPLETED");
              if(isCompleted.equals("1")){
                  if(showResult){           //////if search button is clicked
              new  ResultFrame(regsId).setVisible(true);
              }else
                    JOptionPane.showMessageDialog(null, "You have already attempted the test");
              }else{
                  startTest = true;
                  if(showResult){            //////if search button is clicked
                    JOptionPane.showMessageDialog(null, "You haven't attempted the test");
                  }
              }
        }   else{
            System.out.println("DOES not EXISTS");
            startTest=false;
            JOptionPane.showMessageDialog(null,"No such ID exist");
        }
    }catch(SQLException e){
    JOptionPane.showMessageDialog(null, e.getMessage());
    }
    
    }
    

    
